export const document = pgTable(
  'Document',
  {
    id: uuid('id').notNull().defaultRandom(),
    createdAt: timestamp('createdAt').notNull(),
    title: text('title').notNull(),
    content: text('content'),
    kind: varchar('text', { enum: ['text', 'code', 'image', 'sheet'] })
      .notNull()
      .default('text'),
    userId: uuid('userId')
      .notNull()
      .references(() => user.id),
  },
  (table) => {
    return {
      pk: primaryKey({ columns: [table.id, table.createdAt] }),
    };
  },
);

export type Document = InferSelectModel<typeof document>;

export const suggestion = pgTable(
  'Suggestion',
  {
    id: uuid('id').notNull().defaultRandom(),
    documentId: uuid('documentId').notNull(),
    documentCreatedAt: timestamp('documentCreatedAt').notNull(),
    originalText: text('originalText').notNull(),
    suggestedText: text('suggestedText').notNull(),
    description: text('description'),
    isResolved: boolean('isResolved').notNull().default(false),
    userId: uuid('userId')
      .notNull()
      .references(() => user.id),
    createdAt: timestamp('createdAt').notNull(),
  },
  (table) => ({
    pk: primaryKey({ columns: [table.id] }),
    documentRef: foreignKey({
      columns: [table.documentId, table.documentCreatedAt],
      foreignColumns: [document.id, document.createdAt],
    }),
  }),
);

export type Suggestion = InferSelectModel<typeof suggestion>;

export const stream = pgTable(
  'Stream',
  {
    id: uuid('id').notNull().defaultRandom(),
    chatId: uuid('chatId').notNull(),
    createdAt: timestamp('createdAt').notNull(),
  },
  (table) => ({
    pk: primaryKey({ columns: [table.id] }),
    chatRef: foreignKey({
      columns: [table.chatId],
      foreignColumns: [chat.id],
    }),
  }),
);

export type Stream = InferSelectModel<typeof stream>;

// ADD: OAuth token storage for Google (and future providers)
export const oauthToken = pgTable(
  'OAuthToken',
  {
    id: uuid('id').notNull().defaultRandom(),
    provider: varchar('provider', { length: 32 }).notNull(), // e.g., 'google'
    userId: uuid('userId')
      .notNull()
      .references(() => user.id),
    accessToken: text('accessToken'),
    refreshToken: text('refreshToken'),
    expiresAt: timestamp('expiresAt'), // when the access token expires
    createdAt: timestamp('createdAt').notNull(),
    updatedAt: timestamp('updatedAt').notNull(),
  },
  (table) => ({
    pk: primaryKey({ columns: [table.id] }),
  }),
);

export type OAuthToken = InferSelectModel<typeof oauthToken>;

// Add: WorkflowRun table for FSM authority
export const workflowRun = pgTable('WorkflowRun', {
  id: uuid('id').notNull().defaultRandom().primaryKey(),
  chatId: uuid('chatId').notNull().references(() => chat.id),
  phaseId: varchar('phaseId', { length: 16 }).notNull(), // 'phase-1' | 'phase-2' | 'phase-3'
  status: varchar('status', { length: 32 }).notNull().default('active'), // active|awaiting_approval|complete|error
  step: varchar('step', { length: 64 }).notNull().default('collect_params'),
  params: json('params'),
  requiredInputs: json('requiredInputs'),
  artifacts: json('artifacts'),
  approvals: json('approvals'),
  runInfo: json('runInfo'),
  createdAt: timestamp('createdAt').notNull(),
  updatedAt: timestamp('updatedAt').notNull(),
});

export type WorkflowRun = InferSelectModel<typeof workflowRun>;
